  select 
    distinct `pt`.`TYPE_NAME` AS `type_name`,
    `pr`.`PRODUCT_ID` AS `product_id`,
    `pmst`.`PRODUCT_NAME` AS `product_name`,
    `pcl`.`COLOR_NAME` AS `color_name`,
    `psz`.`SIZE_NAME` AS `size_name`,
    `pr`.`PRICE` AS `price` 
  from 
    ((((`product` `pr` join `product_master` `pmst` on((`pr`.`PRODUCT_MASTER_ID` = `pmst`.`PRODUCT_MASTER_ID`))) join `product_type` `pt` on((`pt`.`TYPE_ID` = `pmst`.`TYPE_ID`))) join `product_color` `pcl` on((`pmst`.`COLOR_ID` = `pcl`.`COLOR_ID`))) join `product_size` `psz` on((`pmst`.`SIZE_ID` = `psz`.`SIZE_ID`))) 
  order by 
    `pt`.`TYPE_NAME`,`pr`.`PRODUCT_ID`,`pcl`.`COLOR_NAME`,`psz`.`SIZE_ID` desc);

